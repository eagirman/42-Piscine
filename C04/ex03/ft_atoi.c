int	ft_atoi(char *str)
{
	int	c;
	int	a;
	int	r;

	a = 1;
	c = 0;
	r = 0;
	while (str[c] <= 32)
		c++;
	while (str[c] == '+' || str[c] == '-')
	{
		if (str[c] == '-')
			a *= -1;
		c++;
	}
	while (str[c] >= '0' && str[c] <= '9')
	{
		r = (str[c] - 48) + (r * 10);
		c++;
	}
	return (r * a);
}
