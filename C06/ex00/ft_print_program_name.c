#include <unistd.h>

int	main(int c, char **arr)
{
	int	i;

	i = 0;
	if (c > 0)
	{
		while (arr[0][i] != '\0')
		{
			write(1, &arr[0][i], 1);
			i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
