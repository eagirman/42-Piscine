#include	<unistd.h>

void	ft_putchar(char i)
{
	write(1, &i, 1);
}

void	ft_putstr(char *str)
{	
	int	c;

	c = 0;
	while (str[c] != '\0')
	{
		ft_putchar(str[c]);
		c++;
	}
}
