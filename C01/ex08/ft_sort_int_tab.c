void	ft_sort_int_tab(int *tab, int size)
{
	int	t;
	int	c;

	c = 0;
	while (c < (size - 1))
	{
		if (tab[c] > tab[c + 1])
		{
			t = tab[c];
			tab[c] = tab[c + 1];
			tab[c + 1] = t;
			c = 0;
		}
		else
			c++;
	}
}
